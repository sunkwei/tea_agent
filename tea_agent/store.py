    def update_msg_rounds(
        self,
        conversation_id: int,
        ai_msg: str,
        is_func_calling: bool,
        rounds: Optional[List[Dict]] = None,
    ):
        """
        更新指定 conversation 行的 ai_msg、is_func_calling 和 rounds_json。

        在 chat_stream 完成后调用，将最终回复和中间工具调用链一次性写入。

        Args:
            conversation_id: conversations 表的行 ID
            ai_msg: AI 最终回复文本
            is_func_calling: 是否使用了工具调用
            rounds: 中间轮次列表（OpenAI API 消息格式）
                    [{"role":"assistant","content":"...","tool_calls":[...]},
                     {"role":"tool","content":"...","tool_call_id":"..."},
                     ...]
        """
        rounds_json = json.dumps(rounds, ensure_ascii=False) if rounds else None
        c = self.conn.cursor()
        c.execute('''
            UPDATE conversations
            SET ai_msg = ?, is_func_calling = ?, rounds_json = ?
            WHERE id = ?
        ''', (ai_msg, 1 if is_func_calling else 0, rounds_json, conversation_id))
        self.conn.commit()
        c.close()

