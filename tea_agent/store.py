    def search_memories(
        self,
        query: str = "",
        category: str = "",
        tags: Optional[List[str]] = None,
        min_importance: int = 0,
        limit: int = 20,
    ) -> List[Dict]:
        """
        搜索记忆：关键词模糊匹配 + 分类过滤 + 标签过滤。

        Args:
            query: 在 content 中模糊匹配的关键词
            category: 按分类过滤（空=不过滤）
            tags: 标签列表，匹配任一标签即可
            min_importance: 最低重要度
            limit: 返回数量上限
        """
        self.cleanup_expired_memories()

        conditions = ["is_active = 1"]
        params: list = []

        if query:
            conditions.append("content LIKE ?")
            params.append(f"%{query}%")

        if category:
            conditions.append("category = ?")
            params.append(category)

        if min_importance:
            conditions.append("importance >= ?")
            params.append(min_importance)

        where = " AND ".join(conditions)

        c = self.conn.cursor()
        c.execute(f'''
            SELECT * FROM memories 
            WHERE {where}
            ORDER BY priority ASC, last_accessed_at DESC
            LIMIT ?
        ''', params + [limit])
        rows = c.fetchall()
        c.close()

        results = [dict(r) for r in rows]

        # 标签过滤（Python 侧，简单匹配）
        if tags:
            tag_set = set(t.lower() for t in tags)
            results = [
                r for r in results
                if tag_set & set(t.strip().lower() for t in (r.get("tags", "") or "").split(","))
            ]

        return results[:limit]

