    def _enforce_critical_limit(self, max_critical: int = 15):
        """
        确保活跃 CRITICAL 记忆不超过 max_critical 条。
        超出时软删除最旧的（按 id ASC，先入先出）。
        """
        c = self.conn.cursor()
        c.execute('''
            SELECT COUNT(*) FROM memories 
            WHERE is_active = 1 AND priority = 0
        ''')
        count = c.fetchone()[0]
        if count >= max_critical:
            # 软删除最旧的（id 最小 = 最早写入）
            overflow = count - max_critical + 1  # +1 为即将插入的腾位
            c.execute('''
                UPDATE memories SET is_active = 0, updated_at = CURRENT_TIMESTAMP
                WHERE id IN (
                    SELECT id FROM memories 
                    WHERE is_active = 1 AND priority = 0
                    ORDER BY id ASC LIMIT ?
                )
            ''', (overflow,))
            self.conn.commit()
            logger.info(f"CRITICAL FIFO 淘汰: 软删除 {overflow} 条旧记忆 (阈值={max_critical})")
        c.close()

